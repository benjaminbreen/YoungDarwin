export function normalizeWildlifeId(input) {
  const raw = typeof input === 'string' ? input : input?.id || input?.specimenId || '';
  if (!raw) return '';
  const compact = String(raw).replace(/[^a-zA-Z0-9_]/g, '').toLowerCase();
  return SPECIES_ALIASES[raw] || SPECIES_ALIASES[compact] || compact;
}

