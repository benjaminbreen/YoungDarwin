export function getWildlifeSpecies(input) {
  return wildlifeCatalog[normalizeWildlifeId(input)] || null;
}

export function getWildlifeBehaviorProfile(input) {
  const entry = getWildlifeSpecies(input);
  return entry?.behavior || null;
}

export function getWildlifeCarryProfile(input) {
  return getWildlifeSpecies(input)?.carry || null;
}

export function getWildlifeAssetId(input) {
  return getWildlifeSpecies(input)?.assetId || null;
}

export function getWildlifeRenderProfile(input) {
  return getWildlifeSpecies(input)?.render || null;
}

export function getWildlifeCollisionRadius(input) {
  return getWildlifeSpecies(input)?.collisionRadius ?? null;
}

export function getWildlifeInteractionHeight(input) {
  return getWildlifeSpecies(input)?.interactionHeight ?? null;
}

export function createAmbientWildlifeSpecimen(item) {
  const speciesId = normalizeWildlifeId(item?.speciesId || item?.species || item?.specimenId || item?.id);
  const entry = wildlifeCatalog[speciesId];
  if (!entry) return null;
  const position = item.position || item.spawnPoint || [0, 0, 0];
  return {
    id: speciesId,
    specimenId: speciesId,
    instanceId: item.instanceId || item.id || `${speciesId}-ambient`,
    name: item.name || entry.englishName,
    latin: item.latin || entry.latinName,
    ontology: item.ontology || entry.category || 'Animal',
    spawnPoint: position,
    behavior: item.behavior || 'ambient',
    sceneScale: Number.isFinite(item.sceneScale) ? item.sceneScale : 1,
    habitatRadiusX: item.habitatRadiusX ?? entry.behavior?.habitatRadiusX,
    habitatRadiusZ: item.habitatRadiusZ ?? entry.behavior?.habitatRadiusZ,
    animationPhase: Number.isFinite(item.animationPhase) ? item.animationPhase : null,
    animationRate: Number.isFinite(item.animationRate) ? item.animationRate : null,
    role: item.role || 'ambient',
  };
}
