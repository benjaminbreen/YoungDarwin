function cloneProfile(profile, biome) {
  return {
    ...DEFAULT_LAND_PROFILE,
    ...profile,
    biome,
    particles: [...(profile?.particles || DEFAULT_LAND_PROFILE.particles)],
  };
}

export function surfaceContactProfileForBiome(biome) {
  const key = typeof biome === 'string' ? biome : '';
  return cloneProfile(PROFILE_BY_BIOME[key] || DEFAULT_LAND_PROFILE, key || 'unknown');
}

export function getSurfaceContactProfile({ x, z, y, zoneId, biome } = {}) {
  const sampleY = Number.isFinite(y)
    ? y
    : (Number.isFinite(x) && Number.isFinite(z) ? terrainHeight(x, z, zoneId) : 0);
  const sampleBiome = biome || (
    Number.isFinite(x) && Number.isFinite(z)
      ? terrainBiomeAt(x, z, sampleY, zoneId)
      : null
  );
  return surfaceContactProfileForBiome(sampleBiome);
}

export function isWaterSurfaceContact(profile) {
  return WATER_KINDS.has(profile?.kind);
}
